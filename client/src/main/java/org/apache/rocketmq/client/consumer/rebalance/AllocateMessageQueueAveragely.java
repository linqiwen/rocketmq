package org.apache.rocketmq.client.consumer.rebalance;

import java.util.ArrayList;
import java.util.List;
import org.apache.rocketmq.client.consumer.AllocateMessageQueueStrategy;
import org.apache.rocketmq.client.log.ClientLogger;
import org.apache.rocketmq.logging.InternalLogger;
import org.apache.rocketmq.common.message.MessageQueue;

/**
 * 平均队列算法
 * <p>
 *     队列数小于consumer数，比如queue:2,consumer:3,queue-0分配给consumer-0，queue-1分配给consumer-1，consumer-2无分配
 * <p>
 *     队列数大于consumer数，比如queue:4,consumer:3,queue-0,queue-1分配给consumer-0,queue2分配给consumer-1,queue-3分配consumer-2
 */
public class AllocateMessageQueueAveragely implements AllocateMessageQueueStrategy {
    private final InternalLogger log = ClientLogger.getLog();

    @Override
    public List<MessageQueue> allocate(String consumerGroup, String currentCID, List<MessageQueue> mqAll,
        List<String> cidAll) {
        if (currentCID == null || currentCID.length() < 1) {
            throw new IllegalArgumentException("currentCID is empty");
        }
        if (mqAll == null || mqAll.isEmpty()) {
            throw new IllegalArgumentException("mqAll is null or mqAll empty");
        }
        if (cidAll == null || cidAll.isEmpty()) {
            throw new IllegalArgumentException("cidAll is null or cidAll empty");
        }

        List<MessageQueue> result = new ArrayList<MessageQueue>();
        if (!cidAll.contains(currentCID)) {
            log.info("[BUG] ConsumerGroup: {} The consumerId: {} not in cidAll: {}",
                consumerGroup,
                currentCID,
                cidAll);
            return result;
        }

        //当前消费者id在消费队列中的位置
        int index = cidAll.indexOf(currentCID);
        //消息队列和消费者集合取模
        int mod = mqAll.size() % cidAll.size();
        //index小于mod，当前消费者多消费一个队列
        int averageSize =
            mqAll.size() <= cidAll.size() ? 1 : (mod > 0 && index < mod ? mqAll.size() / cidAll.size()
                + 1 : mqAll.size() / cidAll.size());
        //index小于mod，从index * averageSize开始，否则从index * averageSize + mod，因为index小于mod，会多消费一个队列
        int startIndex = (mod > 0 && index < mod) ? index * averageSize : index * averageSize + mod;
        //averageSize和mqAll.size() - startIndex取最小值是因为后面的消费者不一定会消费到队列，防止溢出
        int range = Math.min(averageSize, mqAll.size() - startIndex);
        for (int i = 0; i < range; i++) {
            result.add(mqAll.get((startIndex + i) % mqAll.size()));
        }
        return result;
    }

    @Override
    public String getName() {
        return "AVG";
    }
}
